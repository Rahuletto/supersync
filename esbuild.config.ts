import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";

const prod = process.argv[2] === "production";
console.log("BUILD TIME CLIENT ID:", process.env.DEVICE_FLOW_CLIENT_ID);

async function main() {
  const context = await esbuild.context({
    banner: { js: "/* THIS IS A GENERATED/BUNDLED FILE BY ESBUILD */" },
    entryPoints: ["main.tsx"],
    bundle: true,
    external: [
      "obsidian",
      "electron",
      "@codemirror/autocomplete",
      "@codemirror/collab",
      "@codemirror/commands",
      "@codemirror/language",
      "@codemirror/lint",
      "@codemirror/search",
      "@codemirror/state",
      "@codemirror/view",
      "@lezer/common",
      "@lezer/highlight",
      "@lezer/lr",
      ...builtins,
    ],
    format: "cjs",
    target: "es2018",
    logLevel: "info",
    sourcemap: prod ? false : "inline",
    treeShaking: true,
    outfile: "main.js",
    minify: prod,
    define: {
      "process.env.DEVICE_FLOW_CLIENT_ID": JSON.stringify(process.env.DEVICE_FLOW_CLIENT_ID || ""),
    },
  });

  if (prod) {
    await context.rebuild();
    await context.dispose();
  } else {
    await context.watch();
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
